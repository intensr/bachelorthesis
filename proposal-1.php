      <p>Womit sich der Preis als ein weiterer Kritikpunkt offenbart. Obwohl zeitgeistige Produkte, speziell technische Meilensteine der <abbr title="Head Mounted Display bzw. Datenhelm">HMD</abbr>, Entwicklung auf
