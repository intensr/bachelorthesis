    <h2>Beschleunigung von und durch Software</h2>
    <p>Die Ursprünge der Software beginnen vor jener eigentlic<span class="footnote">This is another footnote</span>hen Entwicklung, bereits in der zweiten
Hälfte des 18ten Jahrhunderts während den Anfängen der industriellen Revolution. Diese gilt rückblickend als vergleichend einschneidend wie die Entwicklung des
menschlichen Nomadenlebens zur Sesshaftigkeit. Die relevanten Eigenschaften der industriellen Revolution für die Entwicklung von Software manifestieren sich
durch die Beschleunigung von Technik, Produktivität und Wissenschaft.</p>
    <p class="o-p--tie">Noch deutlicher werden die Beobachtungen durch die steigende Potenz der Rechenleistung von Computersystemen, worauf der
Intel-Mitbegründer Gordon E. Moore erstmals 1965 hinweist. Im sog. Mooreschen Gesetz (Moore's Law) verweist er darauf, dass sich die Rechenleistung welche für
1.000 US-Dollar erhältlich ist sich in immer kürzer werdenden Zeitintervallen seit ca. 1940 verdoppelt. Dies zuletzt alle 18-22 Monate (je nach Quelle) und
bisher um zwölf dezimale Größenordnungen, basierend auf empirischen Beobachtungen.</p>


