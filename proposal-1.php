oder die Hilfe weiterer Statisten, um die Immersion zu erhalten (z.B. für das Führen des Verbindungskabels). Es existieren ebenfalls <span class="c-ref" data-ref-source-id="147" data-ref-type="ref" data-ref-desc="">Vergnügungspark Konzepte</span>
wie <span class="c-ref" data-ref-source-id="148" data-ref-type="ref" data-ref-desc="">The VOID,</span> in denen der Besucher in die jeweilige Realität eintauchen kann und äquivalentes, haptisches, physisches Feedback bekommt. Allerdings wird damit
ebenfalls die Anwendung durch die Gegebenheiten der physischen Welt limitiert. Unabhängig davon, welche aktuellen Versuche unternommen werden eine vollständige
Immersion zu erreichen, sie scheitern oftmals an ihrer Komplexität und den verbundenen Kosten zusätzlich zu den ohnehin vergleichsweise teuren Endgeräten.

