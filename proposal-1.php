      <p>Alle o.g. Entwicklungen des Internets verdeutlichen diverse Erkenntnisse des digitalen Zeitalters mit dem Computer als universelles Meta Medium.</p>
      <p class="o-p--tie">GUIs erweisen sich als Zugangsportale allgemeiner Nutzer, welches sich zu verschiedenen Zeitpunkten der digitalen Entwicklung
wiederholt bestätigen. Sie manifestieren sich in grafischen Benutzeroberflächen als digitale Zeichen bzw. Symbole, wie beispielsweise in der Fenster Logik der
ersten Windows/Apple Betriebssysteme, oder dem sog. burgermenu im mobilen Webdesign.</p>
      <p class="o-p--tie">Viele der ehemaligen Hacker und Aktivisten arbeiten inzwischen in verschiedenen Teilen der IT Branche in jener, neben den
Protagonisten wie staatlichen Vertretern des Gesetzes, den technischen “Enthusiasten” (Hacker und Aktivisten), interagieren nun ebenfalls verstärkt
einflussreiche Unternehmen und Organisationen (Google, Facebook, <abbr title="Electronic Frontier Foundation">EFF</abbr>, usw.) sowie reguläre Nutzer. Eine markante Eigenschaft der liberalen Aktivisten besteht
darin, ungeachtet von persönlichen Eigenschaften und Beziehungen die Grundideale aller Nutzer zu wahren. So <span class="c-ref" data-ref-source-id="123" data-ref-type="ref" data-ref-desc="">kritisiert</span> beispielsweise der Whistleblower Julian
Assange die <abbr title="Electronic Frontier Foundation">EFF</abbr> in ihrer Handlungsweise große Mengen an Geld von großen Unternehmen, wie Google, anzunehmen und damit die Netzneutralität in Gefahr zu bringen,
obwohl Assange von der <abbr title="Electronic Frontier Foundation">EFF</abbr> maßgeblich unterstützt wurde. Dieser Austausch an Kritik ist, ungeahndet von Assanges persönlicher Motivation, eine wichtiger
exemplarischer Beitrag zum Erhalt der Grundeigenschaften des Internets.</p>
      <p class="o-p--tie">Im Wandel des Internets hat der reguläre Nutzer einen Teil seiner Anonymität verloren indem sog. intelligent agents stetig
Informationen der Nutzer absorbieren und auswerten. Der Nutzer hinterlässt bei der Interaktion im Cyberspace, sofern nicht willentlich unterbunden, digitale
Fußspuren. Die Auswertung dieser Daten erfolgen bereits mit steigender Tendenz in Umfang und Auswertung. Eine wichtige Erkenntnis dabei ist, dass Abwandlungen
und Erweiterungen des Meta Mediums irreversibel sind und die Gesellschaft dabei als Prototyp fungiert. Anders als beispielsweise bei Medikamententests existiert
für die digitale Revolution weder eine vorausgehende, ausführliche Untersuchung von technologischen Neuerungen, noch einen festgelegtes Enddatum. Die
Entwicklung schreitet immer weiter voran, ungeahnt ihrer etwaigen Auswirkungen auf ihre Probanden. Die Auswertung erfolgt in der Regel im Nachhinein, sofern sie
überhaupt stattfindet.</p>
      <p class="o-p--tie">Speziell große Technologie Unternehmen spielen eine besondere Rolle innerhalb der gesellschaftlichen Entwicklung, indem sie anders als
die anderen Protagonisten keine klare Position vertreten bzw. verfolgen. Sie positionieren und definieren sich zwar durch ihre Dienstleistungen, Produkte,
Führungen und Mitarbeiter, sind jedoch kein transparenter Indikator für die positive oder negative Entwicklung. Zum einen handeln sie eigennützig,
beispielsweise durch die Verwertung persönlicher Nutzerdaten für das Generieren von Profit. Zum anderen begünstigen ihre leistungsstarken APIs und diversen
Entwicklungszweige den Wachstum neuer Technologien. Sie befinden sich somit in einer besonderen Grauzone in jener treibende Persönlichkeiten wie beispielsweise
Steve Jobs, Alan Kay, Elon Musk, Ray Kurzweil, Larry Page, etc. einen massiven Unterschied ausmachen können.</p>

