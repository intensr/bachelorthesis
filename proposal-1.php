      <h2>Technologischer Fortschritt <span class="u-hide"> Geschichte &amp; Beispiele (Positiv&amp;Negativ)</span></h2>
      <p>Das Streben nach Wissen ist einhergehend mit neuen Technologien und Maschinen welche der Mensch hervorbringt. Die Geschwindigkeit dieser Entwicklung
nimmt, frühestens seit der industriellen Revolution und den damit einhergehenden immer effizienter werdenden Maschinen nicht nur kontinuierlich sondern rapide
und exponentiell zu. Im Zusammenhang mit der Bemessung der Geschwindigkeit technologischen Fortschritts wird aktuell häufig mit Moore's Gesetz in Verbindung
gebracht. Jenes misst und vergleicht die Transistorenanzahl auf Prozessoren wodurch sich schlussendlich der geschichtliche Leistungsanstieg bemessen lässt.
Damit einhergehend eröffnen sich neue Möglichkeiten für abermals noch komplexere Maschinen/Technologien, welche zugleich einen Faktor für den exponentiellen
Wachstum und dessen Maßstab darstellen. Auch wenn solche Maßstäbe regelmäßig an ihre Grenzen stoßen, so ergeben sich historisch gesehen immer neue Maßeinheiten
an denen sich diese exponentielle Entwicklung weiterhin abzeichnen lässt.</p>
      <div><a href="https://www.ted.com/talks/ray_kurzweil_on_how_technology_will_transform_us/transcript" target="tab">VGL Ray Kurzweil - The accelerating
power of technology - Filmed February 2005 at TED2005</a></div>
      <br/>

      <p>Maßgeblich sind in diesem Fall revolutionäre Entdeckungen wie beispielsweise nutzbare Elektrizität, oder die Erfindung des Verbrennungsmotors. Bei der
Entwicklung der Turingmaschine 1936, benannt nach ihrem Entwickler Alan Turing, zeichnet sich diese Entwicklung mit am Stärksten ab. Die Turingmaschine ist
(außerordentlich kurz gefasst) dazu in der Lage komplexe Algorithmen automatisiert zu berechnen. Ihre Entwicklung gilt rückblickend als Pionierarbeit bei der
Entwicklung moderner digitale Rechensysteme, wenngleich Charles Babagge bereits 1812 das Konzept der mechanischen Differenzmaschine vorstellt und viele weitere
Entwicklungen als Grundlage dienen. <span class="c-ref">[*1]</span> Bereits einige Jahre später (1950) beschäftigt Turing sich in seinem Essay “Computing
Machinery and Intelligence” unter anderem mit der Frage “Can machines think?” und treibt die Entwicklung abermals maßgeblich voran. An dieser Stelle werden
zwei
wichtige Erkenntnisse sichtbar. Zum einen wird hier bereits durch die Abstände der Entwicklungsjahre die Beschleunigung der generellen
Entwicklungsgeschwindigkeit deutlich und zum anderen die Weitsicht mit denen Entwickler wie Turing ihre Forschung betreiben. In Turings Essay werden bereits
theoretische Zustände und Konzepte beschrieben, welche sich heutzutage in modernen Computern widerspiegeln. *2 Dazu zählen z.B. die Verwendung von digitalen
Systemen (Hardware/Software) und Machine Learning. Natürlich spielt die Kollaboration verschiedener wissenschaftlicher Themenfelder und die Präsenz/Relevanz
weiterer diverser Entwickler/Forscher eine nicht minder wertvolle Rolle.</p>
      <div>*1 Dionysius Lardner: Babbage's calculating engine. In: The Edinburgh Review. Band 59, Juli 1834</div>
      <div>*2 New Media Reader - Computing Machinery and Intelligence - Alan Turing, 1950</div>
