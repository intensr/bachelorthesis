<span class="c-ref" data-ref-source-id="150" data-ref-type="bsp" data-ref-desc="">Entwicklung</span> jedoch noch nicht auf dem Markt. Unternehmungen, wie beispielsweise <span class="c-ref" data-ref-source-id="153" data-ref-type="bsp" data-ref-desc="">Google Cardboard</span> und Samsung <span class="c-ref" data-ref-source-id="223" data-ref-type="bsp" data-ref-desc="">Gear VR</span> dagegen präsentieren ihre Produkte bereits
kostengünstig, können aber noch nicht der Leistung und dem Gefühl der Immersion von stationären <abbr title="Head Mounted Display bzw. Datenhelm">HMD</abbr>s gerecht werden.</p>
      <p class="o-p--tie">Nach der Analyse der verschiedenen <abbr title="Head Mounted Display bzw. Datenhelm">HMD</abbr> Technologien stellt sich unweigerlich die Frage, wie und ob die Missstände der aktuellen
Technologien zeitnahe behoben werden können, um eine großflächige, gesellschaftliche Nutzung zu erreichen. Nach den anfänglichen Prognosen diverser Blogs,
Enthusiasten und dem Engagement zugehöriger Unternehmen häufen sich zuletzt die Anzahl der Kritiker. Selbst Persönlichkeiten und Verfechter der VR Technologien,
wie Mark Zuckerberg (Facebooks CEO), mahnen zur Geduld im Bezug auf zukünftige VR Entwicklungen.</p>
      <p class="o-p--tie">Zunächst wird dementsprechend eruiert, welche Faktoren für ein erfolgreiches Produkt relevant sind. Durch die bisherige Analyse
zeichnen sich die Erfolgsfaktoren der vergangenen und bis heute präsenten Technologien und deren Einflüsse deutlich ab. Die folgenden Schlüsselkomponenten sind
in ihren Eigenschaften stark untereinander vernetzt, welche sich teilweise verstreut über die verschiedenen Dekaden in Mustern wiederholen. In letzter Instanz
separieren sich diese zu einzelnen Indikatoren für die vage Erfolgsprognose entsprechender Medien bzw. Technologien.</p>

      <ol>
        <li><strong>Balance of Price</strong> Die Angleichung von Preisen eröffnet die jeweilige Technologie dem Massenmarkt, womit sich der Zugriff für eine
breites Spektrum an Konsumentengruppen eröffnet. Prominente Beispiele sind dabei die Entwicklungen der Mikrocomputer und Smartphones (speziell der Erfolg von
Android Smartphones), in denen sich der umfassende Erfolg erst ab der Relativierung des Preises für die jeweilige Technologie einstellt.</li>
        <li><strong>Accessibility</strong> Mit dem freien Zugang auf das jeweilige Medium können neben den ursprünglich beteiligten Unternehmen,
Wissenschaftlern und Forschern ebenfalls versierte Enthusiasten die Entwicklungen maßgeblich beeinflussen, welches sind speziell in der Open Source- und Hacker
Bewegung abzeichnet.</li>
        <li><strong>Mobility</strong> Alan Kays Konzepte des Dynabooks, das Smartphone Segment und Theorien wie das “Internet der Dinge” belegen die Bedeutung
für eine ortsungebundene Verfügbarkeit von Smart Devices als Erfolgsindikator.</li>
        <li><strong>Usability</strong> Die universelle Nutzbarkeit des jeweiligen Mediums, muss gewährleistet werden, damit neben technisch versierten Anwendern
ebenfalls Nutzer aus anderen Themenbereichen Zugriff auf das jeweilige Medium erlangen. Historische Beispiele dafür sind die Einführungen von GUIs und des Web
2.0 als neuartige Schnittstellen der Interaktion sowie abermals die Entwicklungen der Smartphone Industrie.</li>
        <li><strong>Independence and Freedom</strong> Die Wahrung der fundamentalen, ethischen Eigenschaften von gesellschaftlich wirksamen Technologien,
angetrieben durch die frühen Entwicklungen der Hacker Community in Medien wie dem Internet, stellen die Grundlagen der erfolgreichen Kommunikation und
Kollaboration im digitalen Cyberspace dar.</li>
        <li><strong>Universality and Individuality</strong> Das Zusammenspiel von einerseits erfolgreichen Unternehmungen wie Xerox <abbr title="Palo Alto Research Center">PARC</abbr>, Apple, EFF, Google und
weiteren Konsorten sowie die Leistungen von vereinzelten, historisch verknüpften Schlüsselfiguren und Protagonisten wie z.B. John Perry Barlow oder Steve Jobs.
Speziell die Interaktionen und Bewegungsfreiräume zwischen Normen, Gesetzen und der Handlungsspielraum des einzelnen Individuums zeichnet sich in erfolgreichen
Technologien wie dem Internet verstärkt ab.</li>
        <li><strong>Participation and Creation</strong> Indem der Computer als Meta Medium wahrgenommen wird hat der Anwender die Möglichkeit, neue Sub Medien
und Inhalte innerhalb des Meta Mediums zu erstellen. Die Vernetzung sowie die Remix Kultur der zugrunde liegenden Software ermöglicht es aufeinander aufbauende
und kollaborierende Strukturen zu schaffen.</li>
        <li><strong>Zeitgeist</strong> Das umfassende Verständnis und die Analyse kultureller Umstände und Entwicklungen bilden eine elementare Basis das
jeweilige Medium zu platzieren. Das angedachte Medium sollte sich logisch in die aktuellen Bedürfnisse der Gesellschaft einfügen und angedachte, zukünftige
Technologien begünstigen. Dies zeichnet sich primär in den Zukunftsprognosen von Persönlichkeiten wie John von Neumann, Alan Turing, Alan Kay und weiteren
popkulturellen Visionen der frühen Historie ab.</li>
        <li><strong>Interconnectivity</strong> Neben Zeitgeist und Partizipation sind ebenfalls die Auswirkungen auf weitere, unabhängige Themenbereiche ein
entscheidender Faktor. Das Potential hilfreiche Erweiterungen für z.B. Medizin, Kultur, Technik und weitere relevante gesellschaftliche Themenbereiche zu
schaffen verdeutlicht ebenfalls die Relevanz des jeweiligen Mediums.</li>
        <li><strong>Based on Software</strong> Der digitale Markt verdrängt erfolgreich traditionelle Industrien der physischen Welt. Ein Großteil der
erfolgreicher Unternehmungen basiert auf der exzessiven Nutzung von Software. Die Vorteile von Software zeichnet sich durch die Analyse der Historie in ihrer
Flexibilität sich den Gegebenheiten des jeweiligen Vorhabens adaptiv anzupassen ab und ergänzt sich zusätzlich durch alle vorherigen genannten
Komponenten.</li>
      </ol>

      <p>Das Potential für die Erfüllung <strong>jeder</strong> dieser Faktoren manifestiert sich, im Gegensatz zu den klassischen VR Geräten und deren
Anwendungen, in den Entwicklungen von Mobile- bzw. WebVR Technologien. Natürlich sind diese Indikatoren nicht unwiderlegbar oder universell gültig, sie zeigen
jedoch das fundamentale Potential dieser Art von Technologie auf.</p>

      <h2>MobileVR</h2>
      <p>Der mobile Zweig der VR Entwicklung (MobileVR) wird durch die Einführung von <span class="c-ref" data-ref-source-id="153" data-ref-type="bsp" data-ref-desc="" data-ref-hide-kneenote>Googles Cardboard</span> erstmalig popularisiert. Bei Cardboard handelt es sich,
um eine kostengünstige Konstruktion aus Karton und speziellen Linsen in jene ein Smartphone eingelegt wird und somit als <abbr title="Head Mounted Display bzw. Datenhelm">HMD</abbr> fungiert. Das Smartphone muss dabei
bestimmte Sensoren enthalten (Gyroskop, Beschleunigungsmesser und Magnetometer), um die eigene Position im physischen Raum zu berechnen. Stellt das Smartphone
dabei eine bestimmte Rechenleistung bereit, kann eine zugehörige App von Google dazu benutzt werden, diverse Erlebnisse wie beispielsweise die Ansicht von 360°
Videos oder dreidimensionale Spiele zu erleben. Es ist eine vergleichsweise günstige Methode für Konsumenten, sich den Vorzügen von VR Anwendungen anzunähern,
ohne den Kauf von kostenintensiven Geräte wie beispielsweise der HTC Vive oder der Oculus Rift zu tätigen. Smartphones sind inzwischen weit verbreitet und
begünstigen damit die Nutzung, auch wenn die Qualität vergleichsweise schlechter ist und Cardboard keine Controller bereitstellt.</p>
      <p class="o-p--tie">Nach der Einführung von Cardboard erhöht sich das Angebot und die Ausführungen von MobileVR Geräten. Bekannte Smartphone- bzw. Tech
Unternehmen veröffentlichen Produkte wie Samsung <span class="c-ref" data-ref-source-id="223" data-ref-type="bsp" data-ref-desc="" data-ref-hide-kneenote>Gear VR</span>,
Xiaomi <span class="c-ref" data-ref-source-id="224" data-ref-type="bsp" data-ref-desc="">MI VR,</span> usw. Neben den renommierten Herstellern positionieren sich ebenfalls kleinere Anbieter
wie beispielsweise PinchVR mit erfolgreichen Ideen. Weitere Konzepte wie Google Daydream folgen, welches neben einer höheren Qualität der einzelnen Komponenten
zusätzlich einen zugehörigen Controller enthält. Ähnlich wie bei PinchVR verkleinert sich durch die zusätzliche Interaktionsmöglichkeit der Unterschied zwischen
stationären und mobilen Endgeräten, wenngleich die leistungsstärkeren Devices dennoch eine deutlich bessere Darstellung sowie Usability in Form von Immersion
und Interaktion <span class="c-ref" data-ref-source-id="225" data-ref-type="ref" data-ref-desc="">ermöglichen.</span></p>
      <p class="o-p--tie">Während der Ausarbeitung dieser Arbeit ergeben sich fast wöchentlich neue Entwicklungen innerhalb der VR Szene. So erteilt Google immer weitere <span class="c-ref" data-ref-source-id="226" data-ref-type="bsp" data-ref-desc="">Freigaben</span> für Daydream taugliche Smartphones und neben den
Entwicklungen der Hardwareseite ergeben sich weitere Möglichkeiten im <span class="c-ref" data-ref-source-id="160" data-ref-type="bsp" data-ref-desc="">Softwarebereich.</span></p>

